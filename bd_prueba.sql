-- VISTA admin_final
-- En esta vista se agregó un JOIN para que funcione de acuerdo a la nueba bd

CREATE OR REPLACE VIEW `vw_admin_final` AS
SELECT la.`LEGAJO_ALU`                         AS `LEGAJO_ALU`,
       concat(al.`NOMBRE`, ' ', al.`APELLIDO`) AS `ALUMNO`, 
       m.`ASIGNATURA`                          AS `MATERIA`, 
       r.`NOTA`                                AS `NOTA`,
       ef.`LIBRO`                              AS `LIBRO`,
       ef.`FOLIO`                              AS `FOLIO`,
       ef.`FECHA`                              AS `FECHA` 
  FROM `ALUMNO` al 
  JOIN `LEGAJO_ALUMNO` la ON la.`DNI` = al.`DNI`
  JOIN `RINDE`         r  ON r.`ID_ALUMNO` = la.`LEGAJO_ALU`
  JOIN `EXAMEN_FINAL`  ef ON ef.`ID_EXAMEN_FINAL` = r.`ID_EXAMEN`
  JOIN `MATERIA`       m  ON m.`ID_MATERIA` = ef.`ID_MATERIA`;

